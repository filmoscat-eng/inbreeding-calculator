from __future__ import annotations


from dataclasses import dataclass


from .algorithms import compute_relationship_matrix

from .pedigree import Pedigree






def kinship(ped: Pedigree, x: str, y: str) -> float:
    """Коэффициент коанцестрии f(x, y) = A[x, y] / 2.

    Свойства:
    - f(x, x) = 0.5 · (1 + F(x)) — для одной и той же особи с самой собой.
    - f(x, y) = ожидаемый F будущего потомка от пары (x, y) — это и есть
      «потенциальный инбридинг», полезный для планирования спариваний.

    Если хотите узнать, насколько инбредным будет потомок
    от двух конкретных особей, — это и есть kinship. Считаем заранее,
    выбираем пары с низким kinship, получаем здоровое потомство.
    """
    
    
    A = ped.get_relationship_matrix()
    if A is None:
        A = compute_relationship_matrix(ped)
    
    i = ped.index[x]
    j = ped.index[y]
    
    
    
    return float(A[i, j]) / 2.0


def relationship(ped: Pedigree, x: str, y: str) -> float:
    """Коэффициент родства Райта.

    Формула:
        R(x, y) = 2 · f(x, y) / sqrt((1 + F(x)) · (1 + F(y)))

    Числитель — двойная коанцестрия = A[x, y]. Знаменатель — нормировка,
    которая важна, если особи сами инбредны (тогда (1 + F) > 1, и без
    нормировки R был бы завышен).

    R = 0 у независимых особей, 0.5 у родителей-потомков и полных сибсов
    (в неинбредном случае), 1 у клонов / монозиготных близнецов.

    Почему именно эта формула. R показывает «насколько X и Y генетически
    похожи относительно их собственной гомозиготности». Без нормировки
    инбредные особи (у которых уже много одинаковых аллелей сами по себе)
    давали бы R больше, чем «настоящее» родство.
    """
    A = ped.get_relationship_matrix()
    if A is None:
        A = compute_relationship_matrix(ped)
    i = ped.index[x]
    j = ped.index[y]
    
    
    
    fx = A[i, i]      
    fy = A[j, j]      
    
    
    return float(A[i, j] / (fx * fy) ** 0.5)






@dataclass(frozen=True)
class MatingType:
    """Стандартный тип спаривания и его эталонный F.

    frozen=True — объект иммутабельный (нельзя случайно поменять справочник).
    Это защита от ошибок: если кто-то напишет mt.F = 0.5, программа упадёт
    с ошибкой, а не молча испортит справочник.

    Поля:
    - name: человекочитаемое название (например, «Полные сибсы»).
    - F: эталонное значение F потомка для этого типа спаривания.
    - description: короткое пояснение для UI.
    """
    name: str
    F: float
    description: str





MATING_TYPES: tuple[MatingType, ...] = (
    MatingType("Неродственные", 0.0, "Общих предков нет"),
    MatingType("Двоюродные родственники", 0.0625, "Общий прадед/прабабка"),
    MatingType("Полусибсы", 0.125, "Один общий родитель"),
    MatingType("Полные сибсы", 0.25, "Оба родителя общие"),
    MatingType("Отец × дочь / Мать × сын", 0.25, "Близкий инбридинг родитель-потомок"),
)


def classify_inbreeding(F: float, tolerance: float = 1e-3) -> MatingType:
    """Найти в справочнике тип спаривания, ближайший к данному F.

    «Ближайший» — по абсолютному расстоянию. Параметр tolerance оставлен
    в сигнатуре для будущего расширения (например, чтобы возвращать
    «нестандартный тип», если расстояние слишком большое — пока не используется,
    но место уже есть).

    Зачем нужно. Чисто числовой F пользователю мало о чём говорит.
    «F=0.0625» — это сколько? А «двоюродные родственники» — сразу понятно.
    Эта функция переводит число в смысл.
    """
    
    
    
    
    if F < 0:
        F = 0.0
    
    
    
    return min(MATING_TYPES, key=lambda mt: abs(mt.F - F))


def classify_with_distance(F: float) -> tuple[MatingType, float]:
    """Вернуть тип и расстояние до его табличного значения.

    Полезно, если хочется в UI показать «это похоже на полных сибсов,
    но с погрешностью 0.02» — например, у инбредных предков получаются
    нестандартные F, и точное совпадение со справочником редкость.

    Возвращает кортеж (тип, расстояние). Расстояние = abs(F особи − F типа).
    """
    mt = classify_inbreeding(F)
    return mt, abs(mt.F - F)
